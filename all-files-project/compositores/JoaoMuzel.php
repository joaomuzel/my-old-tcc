            <h1>João Müzel</h1>
            <img class="activator" src="../arquivos/imagens/compos1.jpg" id="foto">
            <p>João Pedro Müzel Araújo (Itapeva, 16 de novembro de 2000) é um músico multi-instrumentista, compositor e guitarrista brasileiro, membro da banda de Prog Metal Chord Feeling, e também do Revive.</p><br>
    <p>João sempre foi rodeado por música, desde pequeno ouvia aos CDs e DVDs que seu pai tinha, de bandas como Deep Purple, Iron Maiden, Guns N' Roses, AC/DC, Queen, e entre outras bandas, e era fascinado pelas guitarras de Steve Morse, mesmo antes de tocar guitarra. Sempre foi influenciado pelo seu pai (que vinha de uma imensa família de músicos) a aprender algum instrumento musical, e seu pai era músico multi-instrumentista. Aos 5 anos de idade, João ganhou dos seus pais uma guitarra (de marca barata), mas nunca ligou de fato para a mesma, pois não tinha interesse em aprender algum instrumento musical. Seu primeiro instrumento foi o clarinete, que começou a tocar com 6 anos depois de ver os primos de seu pai tocando na banda sinfônica da igreja Assembleia de Deus, alguns meses depois, passou a ter aulas de música e aprendeu a tocar o instrumento. Tudo ia bem com o clarinete, mas devido a um aparelho bocal que teve que por, teve de abandonar o instrumento, e a banda sinfônica, e foi quando a paixão pela guitarra e a música realmente apareceram. Quando adolescente, João escutava todas as bandas possíveis, e com a internet, passou a conhecer seu próprio gosto pelo Hard Rock e pelo Prog Metal, e por influência de seu pai, passou a aprender guitarra com ele. Seus primeiros riffs de guitarra foram de músicas como "Smoke On The Water" do Deep Purple, "Crazy Train" de Ozzy Osbourne, "Black In Black" do AC/DC, "Knockin' On Heaven's Door" do Guns N' Roses e entre outros. João logo entrou para o grupo de louvor de sua igreja (Presbiteriana), e passou a aprender outro tipo de música, aperfeiçoando cada vez mais a sua música. Em 2014, depois de ter evoluído como músico e como guitarrista, João decidiu seguir a carreira de músico como profissional, passando a estudar guitarra de maneira séria e dedicada, compondo músicas, e tocando em algumas bandas, e essa paixão pela música só foi aumentando com o passar dos anos.</p><br>
    <p>João Müzel, atualmente, está em trabalho em duas bandas, Chord Feeling e Revive!</p>
